package com.example.projectaidm;

public class Conturi {
    private String valuta;
    private double suma;
    private double coefc;
    private double coefv;

    public Conturi() {
    }

    public String getValuta() {
        return valuta;
    }

    public void setValuta(String valuta) {
        this.valuta = valuta;
    }

    public double getSuma() {
        return suma;
    }

    public void setSuma(double suma) {
        this.suma = suma;
    }

    public double getCoefc() {
        return coefc;
    }

    public void setCoefc(double coefc) {
        this.coefc = coefc;
    }

    public double getCoefv() {
        return coefv;
    }

    public void setCoefv(double coefv) {
        this.coefv = coefv;
    }


}
